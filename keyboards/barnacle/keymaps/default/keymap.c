bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    if (record->event.pressed) {
        switch (keycode) {
            case M_ARROW:
                SEND_STRING("->");
                break;
            case M_THIS:
                SEND_STRING("$this->");
                break;
        }
    }
    return true;
}